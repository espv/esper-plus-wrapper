package com.espertech.esper.common.internal.epl.agg.access.core;

import com.espertech.esper.common.internal.bytecodemodel.base.CodegenClassScope;
import com.espertech.esper.common.internal.bytecodemodel.base.CodegenMethod;
import com.espertech.esper.common.internal.bytecodemodel.model.expression.CodegenExpression;
import com.espertech.esper.common.internal.context.aifactory.core.SAIFFInitializeSymbol;
import com.espertech.esper.common.internal.epl.expression.core.ExprForge;

import static com.espertech.esper.common.internal.bytecodemodel.model.expression.CodegenExpressionBuilder.constant;
import static com.espertech.esper.common.internal.bytecodemodel.model.expression.CodegenExpressionBuilder.newInstance;

public class AggregationAgentRewriteStreamForge implements AggregationAgentForge {

    private final int streamNum;

    public AggregationAgentRewriteStreamForge(int streamNum) {
        this.streamNum = streamNum;
    }

    public ExprForge getOptionalFilter() {
        return null;
    }

    public CodegenExpression make(CodegenMethod method, SAIFFInitializeSymbol symbols, CodegenClassScope classScope) {
        return newInstance(AggregationAgentRewriteStream.class, constant(streamNum));
    }
}
